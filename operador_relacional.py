##Quando precisamos realizar comparações, é a hora de utilizarmos os operadores relacionais ou de comparações.



print(5 == 5, " 5 == 5, Comparação se 5 é realmente IGUAL a 5")
print(1 != 5, "1 !=5, Comparação se 1 é Diferente de 5")
print(1>2, "1 > 2, Comparação se 1 é maior que 2")
print(1 < 2, "1 < 2, Comparação se 1 é menor que 2")
print(1 >= 2, "1 >= 2, Comparação se 1 é maior ou igual a 2")
print(2 <= 2 , "2<=2 , Comparação se 2 é Menor igual a 2")
print(1 == True, "1 == true, Cmoparação se 1 é verdadeiro True ")
print(0 == False, "0 == False, Comparação se 0 é Falso" )
